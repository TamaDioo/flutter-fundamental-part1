class NumberStream {
  final StreamController<int> controller = StreamController<int>();

  void addNumberToSink(int newNumber) {
    controller.sink.add(newNumber);
  }

  close() {
    controller.close();
  }

  // Langkah 13 Praktikum 2
  addError() {
    controller.sink.addError("error");
  }
}
